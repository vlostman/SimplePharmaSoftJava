    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();
        String tbl_id = jTable1.getValueAt(fila, 0).toString();
        String tbl_nombre = jTable1.getValueAt(fila, 1).toString();
        String tbl_temperatura_s = jTable1.getValueAt(fila, 2).toString();
        String tbl_valorBase_s = jTable1.getValueAt(fila, 3).toString();
        //PASAR LAS CADENAS A DOBLE (TEMPERATURA, VALOR)
        Double tbl_temperatura = Double.parseDouble(tbl_temperatura_s);
        Double tbl_valorBase = Double.parseDouble(tbl_valorBase_s);
        // PASA LOS DATOS DE LAS VARIABLES A LOS JTEXTFIELD
        jTextField_textoI.setText(tbl_id);
        jTextField_textoN.setText(tbl_nombre);
        jTextField_textoT.setText(tbl_temperatura_s);
        jTextField_textoVB.setText(tbl_valorBase_s);
    }//GEN-LAST:event_jTable1MouseClicked
 
