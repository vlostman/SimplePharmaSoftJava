    public void Refrescar(){
       
        Object[][] rowData = new Object[1][5];
        Object[] miscolumnas = {"Id","Nombre","Temperatura", "valorBase", "costo"};
        DefaultTableModel mimodelo = new DefaultTableModel();
        mimodelo.setColumnIdentifiers(miscolumnas);
        jTable1.setModel(mimodelo);
        Producto p = new Producto();
        List<Producto> lista = p.listarProducto();
        for (Producto pro : lista) {
            rowData[0][0] = pro.getId();
            rowData[0][1] = pro.getNombre();
            rowData[0][2] = pro.getTemperatura();
            rowData[0][3] = pro.getValorBase();
            if (pro.getTemperatura()>20) {rowData[0][4] = pro.getValorBase()*(1.1f);}
            else {rowData[0][4] = pro.getValorBase()* (1.2f);}
            rowData[0][4]=String.format("%.2f", rowData[0][4]);
            mimodelo.addRow(rowData[0]);
        
        } 
        cleartext();
      
            
                        
    }//GEN-LAST:event_jButton_consultarActionPerformed

