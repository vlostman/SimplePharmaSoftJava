    private void jButton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarActionPerformed
        // TODO add your handling code here:
        Producto miproducto = new Producto();
        String id = jTextField_textoI.getText();
        String nombre = jTextField_textoN.getText();
        double temperatura = Double.parseDouble(jTextField_textoT.getText());
        double valorBase = Double.parseDouble(jTextField_textoVB.getText());
        miproducto.setId(id);
        miproducto.setNombre(nombre);
        miproducto.setTemperatura(temperatura);
        miproducto.setValorBase(valorBase);
        if (miproducto.guardarProducto()) {
            JOptionPane.showMessageDialog(this, "Operacion realizada con éxito");
            
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar");
        }
        cleartext();
        
        
    }//GEN-LAST:event_jButton_guardarActionPerformed

